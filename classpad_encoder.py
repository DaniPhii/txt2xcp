#!/usr/bin/env python3

# This program transcodes any characters found in a text file
# from the encoding format used by the Casio ClassPad II (fx-CP400)
# to UTF-8 and vice versa. 
# 
# When converting from UTF-8 to fx-CP400 encoding,
# the resulting file can be packed into a XCP file using the original 
# txt2xcp tool written in C by SnailMath (https://github.com/SnailMath/txt2xcp)
# or txt2xcp.py which is the Python version of the C program.
# 
# 

import sys
import os
import argparse

def clean_header_residue(input_path, output_path=None):
    """
    When extracting the text from an XCP file, it's possible that not
    all header bytes are removed properly; this function removes any 
    leading characters up to the first null character left in the input
    text file.
    """
    try:
        with open(input_path, 'rb') as f:
            content = f.read()

        # Look for the first null byte
        null_byte = b'\x00'
        index = content.find(null_byte)

        # If the null byte is found (index != -1)
        if index != -1:
            # Cut from the next byte onwards
            processed_content = content[index + 1:]
        else:
            # Otherwise, we keep the whole original content
            processed_content = content

        # If an output path is provided, we save the content there
        if output_path:
            with open(output_path, 'wb') as f:
                f.write(processed_content)

        return processed_content

    except FileNotFoundError:
        print(f"🗶 The file '{input_path}' does not exist.")
        sys.exit(1)
    except Exception as e:
        print(f"🗶 An unexpected error occurred: {e}")
        sys.exit(1)

