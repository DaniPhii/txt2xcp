def convert_file(input_path, output_path, convert_mode):
    """
    Transcodes characters from Casio fx-CP400 to UTF-8 and vice versa
    and saves the resulting text file in the provided output path.
    """
    try:
        # 0. Read the original file in binary convert mode (not needed)
        # with open(input_path, 'rb') as f:
        #     content = f.read()

        # 1. Read the original file in binary convert mode
        # removing unnecessary charaters at the beginning
        # of the input file, normally residual fragments of
        # the original XCP file header
        content = clean_header_residue(input_path)

        # 2. Configure the conversion mode
        if convert_mode == "toUTF8":
            # From fx-CP400 (key) to UTF-8 (value)
            transcoding_steps = sorted(CONVERSION_TABLE.items())
            print("⇆ Transcoding characters from Casio fx-CP400 to UTF-8...")
        else:
            # From UTF-8 (value) to fx-CP400 (key)
            transcoding_steps = sorted([(v, k) for k, v in CONVERSION_TABLE.items()], reverse=True)
            # The order is reversed to ensure that double-characters
            # are found first to replace them to their single character
            # equivalent. For example, "e2828be28281" would be replaced
            # by "ecfbecf1" instead of just "ecfc", which is half the size
            # and saves storage space in the calculator.
            print("⇆ Transcoding characters from UTF-8 to Casio fx-CP400...")

        # 3. Applying the replacements sequentially
        for hex_input, hex_output in transcoding_steps:
            bytes_input = bytes.fromhex(hex_input)
            bytes_output = bytes.fromhex(hex_output)
            content = content.replace(bytes_input, bytes_output)

        # 4. Save the file
        with open(output_path, 'wb') as f:
            f.write(content)

        print(f"✔ The file '{output_path}' has been created.")

    except FileNotFoundError:
        print(f"🗶 The input file '{input_path}' does not exist.")
        sys.exit(1)
    except Exception as e:
        print(f"🗶 An unexpected error occurred: {e}")
        sys.exit(1)

if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description="This script transcodes text characters for the Casio ClassPad II (fx-CP400)."
    )

    # At least '--toUTF8' or '--fromUTF8' is needed
    convert_mode_group = parser.add_mutually_exclusive_group(required=True)
    convert_mode_group.add_argument('--toUTF8', action='store_const', const='toUTF8', dest='convert_mode',
                            help='Transcoding characters from Casio fx-CP400 to UTF-8')
    convert_mode_group.add_argument('--fromUTF8', action='store_const', const='fromUTF8', dest='convert_mode',
                            help='Transcoding characters from UTF-8 to Casio fx-CP400')

    # Input and output file paths
    parser.add_argument('input_path', help='Input file path')
    parser.add_argument('output_path', nargs='?', help='Output file path (optional)')

    args = parser.parse_args()

    # If an output file name is not provided, we generate it
    if not args.output_path:
        directory, name = os.path.split(args.input_path)
        prefix = "utf-8_" if args.convert_mode == "toUTF8" else "fx-CP400_"
        args.output_path = os.path.join(directory, f"{prefix}{name}")

    # Run the conversion
    convert_file(args.input_path, args.output_path, args.convert_mode)

